#include <iostream>
#include "Triangle.h"

using namespace std;

int main() {
    double a, b, c;

    // Ввод сторон треугольника с проверкой на корректность
    cout << "Введите длины сторон треугольника (a, b, c): ";
    cin >> a >> b >> c;

    // Проверка корректности данных
    if (cin.fail() || a <= 0 || b <= 0 || c <= 0) {
        cerr << "Ошибка: Введены некорректные данные!" << endl;
        return 1;
    }

    // Создание объекта треугольника
    Triangle t1(a, b, c);

    // Вывод информации о треугольнике
    cout << t1 << endl;

    // Проверка существования треугольника
    if (t1) {
        cout << "Треугольник существует." << endl;
        cout << "Периметр (с приведение к double): " << (double)t1 << endl;
        cout << "Площадь (с помощью унарной операции ~): " << ~t1 << endl;
    } else {
        cout << "Треугольник не существует." << endl;
    }

    // Ввод для второго треугольника
    cout << "Введите длины сторон второго треугольника (a, b, c): ";
    cin >> a >> b >> c;

    if (cin.fail() || a <= 0 || b <= 0 || c <= 0) {
        cerr << "Ошибка: Введены некорректные данные!" << endl;
        return 1;
    }

    // Создание второго треугольника
    Triangle t2(a, b, c);

    // Сравнение площадей двух треугольников
    if (t1 < t2) {
        cout << "Площадь первого треугольника меньше площади второго." << endl;
    } else if (t1 > t2) {
        cout << "Площадь первого треугольника больше площади второго." << endl;
    } else {
        cout << "Площади треугольников равны." << endl;
    }

    return 0;
}
