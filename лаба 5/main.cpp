#include <iostream>
#include "file_operations.h"  // Подключаем файл с функциями для работы с файлами
#include "tasks.h"            // Подключаем файл с функциями для выполнения задач

using namespace std;

int main() {
    int choice;  // Переменная для выбора задачи
    do {
        // Выводим меню с выбором задачи
        cout << "\nВыберите задачу (1-6, 0 для выхода): ";
        cout << "\n1. Задание 1: Подсчёт удвоенных нечётных чисел";
        cout << "\n2. Задание 2: Преобразование матрицы";
        cout << "\n3. Задание 3: Поиск самой дешёвой игрушки";
        cout << "\n4. Задание 4: Квадрат разности максимального и минимального чисел";
        cout << "\n5. Задание 5: Сумма нечётных элементов";
        cout << "\n6. Задание 6: Последний символ каждой строки";
        cout << "\n0. Выход\n";
        cin >> choice;  // Ввод пользователем номера задачи

        // Выполняем выбранную задачу
        switch (choice) {
            case 1: 
                fillBinaryFile("binary1.dat", 10);  // Заполнение бинарного файла случайными числами
                task1("binary1.dat");  // Выполнение задачи 1
                break;
            case 2: 
                fillBinaryFile("binary2.dat", 12);  // Заполнение бинарного файла для матрицы
                task2("binary2.dat", 3, 4);  // Выполнение задачи 2
                break;
            case 3: 
                fillBinaryFileWithStructs("toys.dat", 5);  // Заполнение бинарного файла с игрушками
                task3("toys.dat");  // Выполнение задачи 3
                break;
            case 4: 
                fillTextFile("numbers1.txt", 20);  // Заполнение текстового файла числами
                task4("numbers1.txt");  // Выполнение задачи 4
                break;
            case 5: 
                fillTextFile("numbers2.txt", 20);  // Заполнение текстового файла числами
                task5("numbers2.txt");  // Выполнение задачи 5
                break;
            case 6: 
                fillTextFile("text.txt", 10);  // Заполнение текстового файла строками
                task6("text.txt", "output.txt");  // Выполнение задачи 6
                break;
            case 0: 
                cout << "Выход из программы.\n";  // Выход из программы
                break;
            default: 
                cout << "Неверный выбор. Попробуйте снова.\n";  // Некорректный выбор
        }
    } while (choice != 0);  // Повторяем до тех пор, пока пользователь не выберет выход

    return 0;
}

